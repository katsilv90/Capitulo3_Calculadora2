public class Calculadora {

    //Somar dois números
    public int somar(int a, int b){
        return a+b;
    }

    //Somar três inteiros
    public int somar(int a, int b, int c){
        return a+b+c;
    }

    //Somar dois números decimais
    public double somar(double a, double b){
        return a+b;
    }

    //Subtrair
    public double subtrair(double a, double b){
        return a-b;
    }
}
